todo_list = []

def add_task(task):
    todo_list.append(task)
    print(f"Tugas '{task}' berhasil ditambahkan.")

def delete_task(index):
    try:
        task = todo_list.pop(index - 1)
        print(f"Tugas '{task}' berhasil dihapus.")
    except IndexError:
        print("Indeks tidak valid.")

def list_tasks():
    if not todo_list:
        print("Tidak ada tugas dalam daftar.")
    else:
        print("\nDaftar Tugas:")
        for i, task in enumerate(todo_list, start=1):
            print(f"{i}. {task}")

def reset_tasks():
    todo_list.clear()
    print("Semua tugas berhasil dihapus.")

while True:
    print("\nMenu:")
    print("1. Tambah Tugas")
    print("2. Hapus Tugas")
    print("3. Lihat Semua Tugas")
    print("4. Reset Daftar Tugas")
    print("5. Keluar")
    
    choice = input("Pilih menu: ")

    if choice == "1":
        task = input("Masukkan tugas: ")
        add_task(task)
    elif choice == "2":
        list_tasks()
        index = int(input("Masukkan indeks tugas yang akan dihapus: "))
        delete_task(index)
    elif choice == "3":
        list_tasks()
    elif choice == "4":
        reset_tasks()
    elif choice == "5":
        print("Terima kasih! Sampai jumpa.")
        break
    else:
        print("Pilihan tidak valid.")
